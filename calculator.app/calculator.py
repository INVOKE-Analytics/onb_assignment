def add(a, b):
    """Addition feature"""
    return a + b

def subtract(a, b):
    """Subtraction feature"""
    return a - b

def multiply(a, b):
    """Multiplication feature"""
    return a * b

def divide(a, b):
    """Division feature"""
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero"

